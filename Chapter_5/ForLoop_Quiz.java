package Chapter_5;

public class ForLoop_Quiz {
    public static void main(String[] args) {
        int n=5;
        for(int i=0; i<n; i++){
            System.out.println(2*i+1);
        }

        // Decrementing for loop

        System.out.println("\nDecrementing for loop");
        for(int j=7; j!=0; j--){
            System.out.println(j);
        }
    }
}
