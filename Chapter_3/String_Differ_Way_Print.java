public class String_Differ_Way_Print {
    public static void main(String[] args) {
        System.out.print("My name is ");
        System.out.println("Atul Dubey");
        int a=10;
        float b=5.6f;
        float sum=a+b;
        System.out.printf("The sum of %.2f is a two number. \n", sum);
        System.out.format("The sum of %8.2f is a two number.", sum);
    }
}
